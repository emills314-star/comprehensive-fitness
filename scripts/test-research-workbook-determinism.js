"use strict";

const assert = require("node:assert/strict");
const crypto = require("node:crypto");
const fs = require("node:fs");
const path = require("node:path");
const { spawnSync } = require("node:child_process");
const ExcelJS = require("exceljs");
const JSZip = require("jszip");

const ROOT = path.resolve(__dirname, "..");
const ARTIFACT_ROOT = path.join(ROOT, "artifacts");

function run(command, args, options = {}) {
  const result = spawnSync(command, args, {
    cwd: options.cwd || ROOT,
    env: { ...process.env, ...(options.env || {}) },
    encoding: "utf8",
    windowsHide: true
  });
  if (result.error) throw result.error;
  assert.equal(
    result.status,
    0,
    `${command} ${args.join(" ")} failed in ${options.cwd || ROOT}\n${result.stdout || ""}${result.stderr || ""}`
  );
  return String(result.stdout || "").trim();
}

function sha256(input) {
  const bytes = Buffer.isBuffer(input) ? input : fs.readFileSync(input);
  return crypto.createHash("sha256").update(bytes).digest("hex");
}

function assertManifestHashParity(repositoryRoot, label) {
  const researchRoot = path.join(repositoryRoot, "research_database");
  const manifest = JSON.parse(fs.readFileSync(path.join(researchRoot, "exports", "json", "manifest.json"), "utf8"));
  let checked = 0;
  Object.entries(manifest.tables).forEach(([table, entry]) => {
    const files = {
      csv_sha256: path.join(researchRoot, "exports", "csv", `${table}.csv`),
      json_sha256: path.join(researchRoot, "exports", "json", `${table}.json`),
      schema_sha256: path.join(researchRoot, "schema", `${table}.schema.json`)
    };
    Object.entries(files).forEach(([field, file]) => {
      assert.equal(sha256(file), entry[field], `${label}: ${table}.${field} must match the generated bytes`);
      checked += 1;
    });
  });
  return checked;
}

function materializeArchive(candidateTree, temporaryRoot, name, autocrlf) {
  const archiveFile = path.join(temporaryRoot, `${name}.tar`);
  const repositoryRoot = path.join(temporaryRoot, name);
  fs.mkdirSync(repositoryRoot);
  run("git", [
    "-c", `core.autocrlf=${autocrlf}`,
    ...(autocrlf ? ["-c", "core.eol=crlf"] : []),
    "archive", "--format=tar", `--output=${archiveFile}`, candidateTree
  ]);
  run("tar", ["-xf", archiveFile, "-C", repositoryRoot]);
  return repositoryRoot;
}

function buildResearchDatabase(repositoryRoot) {
  const nodePath = [path.join(ROOT, "node_modules"), process.env.NODE_PATH].filter(Boolean).join(path.delimiter);
  run(process.execPath, ["scripts/build-research-database.js"], { repositoryRoot, cwd: repositoryRoot, env: { NODE_PATH: nodePath } });
}

async function assertWorkbookMatchesSource(repositoryRoot, label) {
  const sourcePath = path.join(repositoryRoot, "research_database", "source", "database.js");
  delete require.cache[require.resolve(sourcePath)];
  const { data, tableColumns, VERSION, REVIEW_DATE } = require(sourcePath);
  const workbookPath = path.join(repositoryRoot, "research_database", "workbook", `male_exercise_science_database_v${VERSION}.xlsx`);
  const zip = await JSZip.loadAsync(fs.readFileSync(workbookPath));
  const entryNames = Object.keys(zip.files);
  assert.deepEqual(entryNames, [...entryNames].sort(), `${label}: ZIP entries must use deterministic lexical order`);
  const fixedEntryTime = new Date(`${REVIEW_DATE}T00:00:00.000Z`).getTime();
  entryNames.forEach((name) => {
    assert.equal(zip.files[name].date.getTime(), fixedEntryTime, `${label}: ${name} must use the fixed review-date timestamp`);
  });
  const workbook = new ExcelJS.Workbook();
  await workbook.xlsx.readFile(workbookPath);
  const tables = Object.entries(data);
  assert.equal(workbook.worksheets.length, tables.length, `${label}: workbook must contain one sheet per source table`);
  let checkedCells = 0;
  tables.forEach(([table, rows], tableIndex) => {
    const sheet = workbook.worksheets[tableIndex];
    const columns = tableColumns[table];
    assert.deepEqual(columns.map((_, index) => sheet.getCell(1, index + 1).value), columns, `${label}: ${table} headers`);
    assert.equal(sheet.rowCount, rows.length + 1, `${label}: ${table} row count`);
    rows.forEach((row, rowIndex) => columns.forEach((field, columnIndex) => {
      assert.deepEqual(sheet.getCell(rowIndex + 2, columnIndex + 1).value, row[field] ?? null, `${label}: ${table}[${rowIndex}].${field}`);
      checkedCells += 1;
    }));
  });
  return { workbookPath, sheets: workbook.worksheets.length, checkedCells, zipEntries: entryNames.length };
}

async function main() {
  fs.mkdirSync(ARTIFACT_ROOT, { recursive: true });
  const temporaryRoot = fs.mkdtempSync(path.join(ARTIFACT_ROOT, "research-workbook-determinism-"));
  try {
    const candidateTree = run("git", ["-c", "core.fsmonitor=false", "write-tree"]);
    const lfRoot = materializeArchive(candidateTree, temporaryRoot, "build-lf", false);
    const autocrlfRoot = materializeArchive(candidateTree, temporaryRoot, "build-autocrlf", true);
    const candidateWorkbook = path.join(lfRoot, "research_database", "workbook", "male_exercise_science_database_v3.0.0.xlsx");
    const candidateWorkbookHash = sha256(candidateWorkbook);
    assert.equal(sha256(path.join(autocrlfRoot, "research_database", "workbook", "male_exercise_science_database_v3.0.0.xlsx")), candidateWorkbookHash, "LF and autocrlf archives must preserve the committed workbook bytes");

    buildResearchDatabase(lfRoot);
    buildResearchDatabase(autocrlfRoot);
    const lfHashCount = assertManifestHashParity(lfRoot, "LF clean build");
    const autocrlfHashCount = assertManifestHashParity(autocrlfRoot, "autocrlf clean build");
    assert.equal(autocrlfHashCount, lfHashCount, "Both clean builds must verify the same manifest surface");

    const lfWorkbook = await assertWorkbookMatchesSource(lfRoot, "LF clean build");
    const autocrlfWorkbook = await assertWorkbookMatchesSource(autocrlfRoot, "autocrlf clean build");
    const lfWorkbookHash = sha256(lfWorkbook.workbookPath);
    const autocrlfWorkbookHash = sha256(autocrlfWorkbook.workbookPath);
    assert.equal(autocrlfWorkbookHash, lfWorkbookHash, "Two clean builds must produce byte-identical XLSX files");
    assert.equal(lfWorkbookHash, candidateWorkbookHash, "A clean build must reproduce the committed XLSX bytes");
    assert.equal(autocrlfWorkbook.checkedCells, lfWorkbook.checkedCells, "Both workbooks must validate the same source cells");

    console.log(`Research workbook determinism passed (${lfWorkbook.sheets} sheets; ${lfWorkbook.checkedCells} source cells; ${lfWorkbook.zipEntries} fixed ZIP entries; ${lfHashCount} manifest hashes).`);
    console.log(`Workbook SHA-256: ${lfWorkbookHash}`);
  } finally {
    fs.rmSync(temporaryRoot, { recursive: true, force: true });
  }
}

main().catch((error) => {
  console.error(error && error.stack || error);
  process.exitCode = 1;
});
